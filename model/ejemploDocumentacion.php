<?php

/* Funcion suma
 * Esta función toma como parámetros dos números
 * @autor: Laura
 * @versión: 1.0
 * @package FuncionesMatemáticas
 * @param number $a
 * @param number $b
 * @return number
*/

function suma($a,$b){
    return $a+$b;
}

/*  Funcion resta
 *@param
 * Esta función toma como parámetros dos números
 * @autor: Laura
 * @versión: 1.0
 * @package FuncionesMatemáticas
 * @param number $a
 * @param number $b
 * return number
 * 
*/
function resta($a,$b){
    return $a-$b;
}

